def get_video_id_for_course(course_title, lecture_num, section_num):
    """Get appropriate video ID based on course title and lecture position"""
    course_lower = course_title.lower()
    
    # Determine topic - expanded matching
    if 'python' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['python']
    elif 'django' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['django']
    elif 'javascript' in course_lower or 'js' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['javascript']
    elif 'react' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['react']
    elif 'node' in course_lower or 'nodejs' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['nodejs']
    elif 'data' in course_lower and 'science' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['data-science']
    elif 'machine' in course_lower and 'learning' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['machine-learning']
    elif 'docker' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['docker']
    elif 'kubernetes' in course_lower or 'k8s' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['kubernetes']
    elif 'postgres' in course_lower or 'postgresql' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['postgresql']
    elif 'vue' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['vue']
    elif 'aws' in course_lower or 'amazon' in course_lower or 'cloud' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['aws']
    elif 'git' in course_lower or 'github' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['git']
    elif 'api' in course_lower or 'rest' in course_lower or 'graphql' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['api']
    elif 'shopify' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['shopify']
    elif 'typescript' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['typescript']
    elif 'angular' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['angular']
    elif 'mongo' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['mongodb']
    elif 'cyber' in course_lower or 'security' in course_lower:
        videos = VIDEO_IDS_BY_TOPIC['cybersecurity']
    else:
        videos = VIDEO_IDS_BY_TOPIC['default']
    
    # Use lecture position to select video (cycle through available videos)
    # This ensures different videos for different lectures
    video_index = ((section_num - 1) * 3 + (lecture_num - 1)) % len(videos)
    return videos[video_index]
